"use strict";

// mathmatic operations
function addition(operator, firstNumber, secondNumber) {
  return firstNumber + secondNumber;
}

function subtraction(operator, firstNumber, secondNumber) {
  return firstNumber - secondNumber;
}

function multiplication(operator, firstNumber, secondNumber) {
  return firstNumber * secondNumber;
}

function division(operator, firstNumber, secondNumber) {
  return firstNumber / secondNumber;
}

function percentage(operator, firstNumber, secondNumber) {
  return (firstNumber / 100) * secondNumber;
}
